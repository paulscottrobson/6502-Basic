Command_Dim: ;; [dim]
		.variable_createarray				; create an array.
		lda 	(codePtr),y 				; see what follows.
		iny
		cmp 	#TKW_COMMA 					; comma follows, go back and do next
		beq 	Command_Dim
		dey 								; unpick get 
		rts

		.send code
		