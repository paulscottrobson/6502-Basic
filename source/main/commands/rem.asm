Command_Rem: ;; [rem]
Command_Rem2: ;; [']
		lda 	(codePtr),y 				; what follows
		cmp 	#TOK_STR 					; if it is a string then skip it.
		bne 	_CRNotString
		;
		iny
		tya
		sec
		adc 	(codePtr),y 				; get string size, add to Y, +1 for the length
		tay
_CRNotString:
		rts		

		.send code