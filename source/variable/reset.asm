; ************************************************************************************************
;
;								Reset all hash tables
;
; ************************************************************************************************

HashTableReset:		;; <reset>
		ldx 	#6*2*hashTableSize
		lda 	#0
_HTRClear:
		sta 	hashTables-1,x
		dex
		bne 	_HTRClear		
		rts

		.send code	
