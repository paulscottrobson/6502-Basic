; ************************************************************************************************
;
;								Reset all hash tables
;
; ************************************************************************************************

HashTableReset:		;; <reset>
		ldx 	#6*2*hashTableSize			; 6 types, 2 bytes per table entry.
		lda 	#0
_HTRClear:
		sta 	hashTables-1,x
		dex
		bne 	_HTRClear		
		rts

		.send code	

