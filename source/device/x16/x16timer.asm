;
;								Read 60Hz timer into YA
;
; ************************************************************************************************

IOReadTimer: ;; <timer>
		jsr 	$FFDE
		pha
		txa
		tay
		pla
		rts

;
;		50Hz clocks, this is not a microtimer, so a x 3 / 4 approximation should be fine
;

		.send code