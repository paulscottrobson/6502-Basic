;
;								Read 60Hz timer into YA
;
; ************************************************************************************************

IOReadTimer: ;; <timer>
		jsr 	KNLReadTimer 				; reads timer into YXA so we want only YA
		pha
		txa
		tay
		pla
		rts
;
;	Porting note: The system is designed around 60Hz clock ticks. For 50Hz clocks, 
;   a x 3 / 4 approximation should be fine.
;

		.send code

