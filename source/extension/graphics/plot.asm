; ************************************************************************************************
;
;										Plot Pixel
;
; ************************************************************************************************

Command_Plot: 	;; [plot]
		lda 	#PlotHandler & $FF
		ldx 	#PlotHandler >> 8
		jsr 	GHandler
		rts

; ************************************************************************************************
;
;									Handler to Plot
;
; ************************************************************************************************

PlotHandler:
		ldy 	#gX2-gX1
		jsr 	SetupXY 					; set up X2,Y2 to draw e.g. plot to 100,100
		bcs 	_PHExit 					; off screen, return with CS.
		jsr 	gdPlotInk 					; pixel in Ink.
_PHExit:rts

		.send 	code