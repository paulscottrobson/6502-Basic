;
;									Draw Image (Sprite Graphic)
;
; ************************************************************************************************

Command_Paint: 	;; [paint]
		lda 	#ImageHandler & $FF
		ldx 	#ImageHandler >> 8
		jsr 	GHandler
		rts

; ************************************************************************************************
;
;									Handler to Image
;
; ************************************************************************************************

ImageHandler:
		lda 	#TestImageAccess & $FF
		ldx 	#TestImageAccess >> 8
		jmp 	ImageRenderer

		.send 	code
				